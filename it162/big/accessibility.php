Topic #3: Usable Accessibility in Web Design

The most basic UX goal is to provide a visitor, with any level of cognitive and physical abilities, with a comfortable and engaging experience. If the site does not meet any user’s needs, it is a failure on the part of the Design Team. However, in web design, usability and accessibility are not the same thing but are directly related. Usability is the ease and learnability of using a website to access resources. While accessibility is the ability to utilize the assistive technology to allow equal access to resources as user. 

However, it is not just a question of equal access to resources for individuals with additional needs but also of equity of accessing said resources. The option of equal usage is not the same of the functionality of use of use For example, having the use to a screen reader on a medical or financial services website is useless if the features do not read clearly or slowly enough for the user to complete a transaction or schedule an appointment. 

Best Practices for Webpage Design

Disabilities that require assistive technology are typically related to permanent states such as auditory, visual, limited motor and mobility. However, there also circumstantial states such as a broken arm or eye surgery. All of which require assistance to successfully navigate a website. 

To be sure the designer is considering all audiences, ensure all content meets the P.O.U.R* criteria: 

•	Is the content perceivable?
•	Are the interface elements operable?
•	Are the content and controls understandable? 
•	Does the site offer robust content to work with current and future technologies? 

There are many resources available that offer quick references of good design practice. The following are some of the most basic practices taken for the A11y Project’s website. : 

•	Validate all HTML and CSS
•	Create linear content flow with appropriate spacing in a logical sequence
•	Eliminate horizontal scrolling.
•	Screen readers need to be compatible with all browsers and properly announce calls to action. 
•	Always make sure HTML<img> include an alternative description “alt”
•	Do not use autoplay  and ensure all media can easily be paused.
•	Use appropriately sized and high contrast colors for borders, icons, and text at any size.
•	Make sure that error, warning, and success states are not visually communicated only by color.
•	Avoid flashing or strobe light features which might trigger seizures.
•	Ensure that screens rotate to any orientation.
•	Ensure that the tab button and arrow keys are functioning to allow for keyboard navigation only. 



Keeping Up with the User’s Needs

Test, test, and more testing. User testing is the best way to ensure your site is accessible and usable for all. Don’t assume the needs of a person with disabilities. Do routine user testing with the appropriate audience. Since technology changes literally minute by minute, do the testing regularly. There are not only advancements in technology but also related medical fields. 

Most of all keep websites simple and logical, which should apply to any website considering usability. If it is confusing to a standard user, the design flaws are going to be amplified for those who need accommodations. 
